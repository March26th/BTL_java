    private void btnThemNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNhapActionPerformed
        if (txtMaHDNhap.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa nhập mã nhập");
        } else if (txtGiaNhap.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa nhập giá");
        } else if (txtSoLuongNhap.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa nhập số lượng");
        } else if (ExceptionString.isNumber(txtGiaNhap.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Giá nhập phải là 1 số");
        } else if (ExceptionString.isNumber(txtSoLuongNhap.getText()) == false) {
            JOptionPane.showMessageDialog(null, "số lượng nhập phải là 1 số");
        } else {
            try {
                if (ExceptionString.isUnicode(txtMaHDNhap.getText())) {
                    JOptionPane.showMessageDialog(null, "Mã HD không chứa kí tự đặc biệt");
                } else if (ExceptionString.isSpace(txtMaHDNhap.getText())) {
                    JOptionPane.showMessageDialog(null, "Mã HD không khoảng trắng");
                } else {
                    try {
                        int tt = Integer.parseInt(txtSoLuongNhap.getText()) * Integer.parseInt(txtGiaNhap.getText());
                        DisplayValueModel dvm = (DisplayValueModel) jComboBoxMaMayTinhNhap.getSelectedItem();
                        DisplayValueModel dvmncc = (DisplayValueModel) jComboBoxMaNhaCungCapNhap.getSelectedItem();
                        String sql = "Insert into  HoaDonNhap(MaHDN, MaMayTinh, MaNCC, SoLuong, ThanhTien, NgayNhap, GiaNhap) values (?,?,?,?,?,CONVERT(DATETIME,?, 102),?)";

                        pst = conn.prepareStatement(sql);

                        pst.setString(1, txtMaHDNhap.getText());
                        pst.setString(2, dvm.valueMember.toString());
                        pst.setString(3, dvmncc.valueMember.toString());
                        pst.setString(4, txtSoLuongNhap.getText());

                        pst.setString(7, txtGiaNhap.getText());
                        pst.setString(5, String.valueOf(tt));
                        pst.setString(6, datenow);
                        int i = pst.executeUpdate();
                        if (i > 0) {
                            JOptionPane.showMessageDialog(null, "Lưu thành công");
                            cleanN();
                        } else {
                            JOptionPane.showMessageDialog(null, "Lưu thất bại");
                        }

                    } catch (SQLException | HeadlessException e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                    updateTableHoaDonNhap();
                }
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(NhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

