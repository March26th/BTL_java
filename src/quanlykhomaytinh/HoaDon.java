    public void fillComboMaMayTinh() {
        DefaultComboBoxModel dcb = new DefaultComboBoxModel();
        try {
            String sql = "Select * from MayTinh";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                String maMayTinh = rs.getString("MaMayTinh");
                String tenMayTinh = rs.getString("TenMayTinh");
                DisplayValueModel dvn = new DisplayValueModel(tenMayTinh, maMayTinh);
                dcb.addElement(dvn);
            }
            jComboBoxMaMayTinhNhap.setModel(dcb);
            jComboBoxMaMayTinhXuat.setModel(dcb);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

